--- Append content after the base string (joins with newlines)
--- @param base string The base content
--- @param ... string Content lines to append
--- @return string Combined content
function append(base, ...)
    return base .. table.concat({...}, "\n")
end

--- Load a file from a resource
--- @param resourceName string The resource to load from
--- @param filePath string The file path within the resource
--- @return string|nil File content
function loadFile(resourceName, filePath)
    return LoadResourceFile(resourceName, filePath)
end

--- Append content after "QBShared.Items = {" pattern
--- @param fileContent string The file content to modify
--- @param insertContent string The content to insert
--- @return string Modified file content
function appendQB(fileContent, insertContent)
    fileContent = fileContent:strtrim()
    return fileContent:gsub("QBShared.Items = {", function(match)
        return match .. "\n" .. insertContent .. "\n"
    end)
end

